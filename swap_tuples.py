#Q2) WAP to swap two tuples in python.


# Define two tuples
tuple1 = (45, 32, 63)
tuple2 = (80, 55, 64)

# Print original tuples
print("Original tuples:")
print("tuple1:", tuple1)
print("tuple2:", tuple2)

# Swap the tuples
tuple1, tuple2 = tuple2, tuple1

# Print swapped tuples
print("\nSwapped tuples:")
print("tuple1:", tuple1)
print("tuple2:", tuple2)

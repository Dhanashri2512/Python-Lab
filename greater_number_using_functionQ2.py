#Q.2)WAP to find maximum among two number.


n1 = int(input("Enter the first number: "))
n2 = int(input("Enter the second number: "))

def maximum(n1, n2):
    if n1 > n2:
        print(" first number is greater:",n1)
    else:
        print(" second number is greater:",n2) 


maximum(n1,n2)

